    @Override
    public String[] getSpawnNames()
    {
        int n = getSpawnCount();
        String[] names = new String[n];
        // 18 Gull, 9 Gulliver, 18 Giant Squirrel, 9 Beaver
        for (int i = 0; i < n; i++)
        {
            if (i < 18) names[i] = "Gull";
            else if (i < 27) names[i] = "Gulliver";
            else if (i < 45) names[i] = "Giant Squirrel";
            else names[i] = "Beaver";
        }
        return names;
    }

